public class les_boucles {
    public static void main(String[] args) {
        int i;
        //la bouvle pour
        for(i=1;i<=10;i++)

        {
            System.out.println(i);
        }

        //la boucle tant que
        i=1;
        while(i<=10) {
            System.out.println(i);
            i++;
        }
        i=34;
        do{
            System.out.println(i);
        }while(i<=10);

    }

}
